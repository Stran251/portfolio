--which authors have written more than 1 million words total
select
    author,
    sum(words) as total_words
from 
    books
group by 
    author
having
    total_words > 1000000;

--which authors averaged more than 150,000 words per book
select
    author,
    avg(words) as avg_words
from 
    books
group by 
    author
having
    avg_words > 150000;


